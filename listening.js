// Play audio using 3-tier TTS chain (Inworld -> Google Cloud -> Browser)
async function playListeningAudio() {
    console.log('=== playListeningAudio CALLED (Three-Tier System) ===');
    console.log('listeningPlaysLeft:', listeningPlaysLeft);
    console.log('isListeningPlaying:', isListeningPlaying);

    if (listeningPlaysLeft <= 0 || isListeningPlaying) {
        console.log('Blocked: playsLeft=' + listeningPlaysLeft + ', isPlaying=' + isListeningPlaying);
        return;
    }

    console.log('Proceeding with playback...');

    const exercise = listeningExercises[currentListeningLevel][currentListeningIndex];
    const playBtn = document.getElementById('play-audio-btn');

    console.log('Exercise:', exercise ? exercise.title : 'NOT FOUND');
    console.log('Play button:', playBtn ? 'FOUND' : 'NOT FOUND');

    // Update UI immediately
    isListeningPlaying = true;
    playBtn.textContent = '🔊 Generating audio...';
    playBtn.disabled = true;
    playBtn.style.opacity = '0.7';

    // Clean text
    let cleanText = exercise.text
        .replace(/—/g, ' ')
        .replace(/\n/g, ' ')
        .replace(/\s+/g, ' ')
        .trim();

    console.log('Text length:', cleanText.length, 'characters');

    try {
        // Tier 1: Try Inworld TTS (Primary - Cheapest)
        console.log('🔄 Trying Tier 1: Inworld TTS...');
        playBtn.textContent = '🔊 Using Inworld TTS...';

        const response = await fetch(
            'https://api.inworld.ai/tts/v1/voice:stream',
            {
                method: 'POST',
                headers: {
                    'Authorization': `Basic ${INWORLD_API_KEY}`,
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    text: cleanText,
                    voiceId: INWORLD_VOICE_ID,
                    modelId: INWORLD_MODEL,
                    audio_config: {
                        audio_encoding: "MP3",
                        sample_rate_hertz: 48000
                    }
                })
            }
        );

        if (!response.ok) {
            throw new Error(`Inworld API error: ${response.status} ${response.statusText}`);
        }

        console.log('✅ Audio received from Inworld TTS');

        // Parse newline-delimited JSON response
        const responseText = await response.text();
        const lines = responseText.trim().split('\n').filter(line => line.trim());

        // Collect all audio chunks
        const audioChunks = [];
        for (let i = 0; i < lines.length; i++) {
            try {
                const chunk = JSON.parse(lines[i]);
                if (chunk.result && chunk.result.audioContent) {
                    audioChunks.push(chunk.result.audioContent);
                }
            } catch (parseError) {
                console.warn(`Failed to parse chunk ${i + 1}:`, parseError);
            }
        }

        if (audioChunks.length === 0) {
            throw new Error('No audio content received from Inworld');
        }

        // Combine and decode base64 audio
        const combinedBase64 = audioChunks.join('');
        const binaryString = atob(combinedBase64);
        const bytes = new Uint8Array(binaryString.length);
        for (let i = 0; i < binaryString.length; i++) {
            bytes[i] = binaryString.charCodeAt(i);
        }

        const audioBlob = new Blob([bytes], { type: 'audio/mp3' });
        const audioUrl = URL.createObjectURL(audioBlob);

        // Update UI
        playBtn.textContent = '🔊 Loading audio...';

        // Create and play audio
        const audio = new Audio(audioUrl);

        // Wait for audio to be fully loaded before playing
        await new Promise((resolve, reject) => {
            audio.onloadeddata = () => {
                console.log('✅ Audio data loaded');
            };

            audio.oncanplaythrough = () => {
                console.log('✅ Audio fully buffered and ready');
                resolve();
            };

            audio.onerror = (e) => {
                console.error('❌ Audio loading error:', e);
                reject(e);
            };

            audio.load();
        });

        // Add extra 1000ms delay to ensure complete buffering
        console.log('⏳ Waiting 1000ms for complete buffering...');
        await new Promise(resolve => setTimeout(resolve, 1000));

        // Now play the audio
        playBtn.textContent = '🔊 Playing...';
        console.log('▶️ Starting playback now...');

        audio.onplay = () => {
            console.log('✅ Audio playback started');
        };

        audio.onended = () => {
            console.log('✅ Audio playback completed');
            finishPlayback();
            URL.revokeObjectURL(audioUrl);
        };

        audio.onerror = (e) => {
            console.error('❌ Audio playback error:', e);
            alert('Error playing audio. Please try again.');
            finishPlayback();
            URL.revokeObjectURL(audioUrl);
        };

        await audio.play();

    } catch (error) {
        console.error('❌ Tier 1 (Inworld) failed:', error);
        console.log('🔄 Trying Tier 2: Google Cloud TTS...');

        // Tier 2: Try Google Cloud TTS
        playBtn.textContent = '🔊 Using Google Cloud voice...';

        try {
            await playGoogleCloudTTS(cleanText);
            finishPlayback();
        } catch (googleError) {
            console.error('❌ Tier 2 (Google Cloud) failed:', googleError);
            console.log('🔄 Trying Tier 3: Browser TTS...');

            // Tier 3: Fallback to browser TTS
            playBtn.textContent = '🔊 Using browser voice...';

            try {
                await playFallbackTTS(cleanText);
                finishPlayback();
            } catch (browserError) {
                console.error('❌ All 3 TTS tiers failed:', browserError);
                alert('Audio generation failed. Please use the "Show Transcript" button.');
                finishPlayback();
            }
        }
    }

    function finishPlayback() {
        isListeningPlaying = false;
        listeningPlaysLeft--;

        console.log('Plays left:', listeningPlaysLeft);

        // Update button
        if (listeningPlaysLeft > 0) {
            playBtn.innerHTML = `🔊 Play Audio (<span id="plays-left">${listeningPlaysLeft}</span> plays left)`;
            playBtn.disabled = false;
            playBtn.style.opacity = '1';
            playBtn.onclick = playListeningAudio;
        } else {
            playBtn.textContent = '🔊 No plays left';
            playBtn.disabled = true;
            playBtn.style.opacity = '0.5';
        }

        // Show questions
        showListeningQuestions();
    }
}

// Google Cloud TTS fallback (Tier 2)
async function playGoogleCloudTTS(text) {
    console.log('🔄 Trying Google Cloud TTS...');

    try {
        const response = await fetch(
            `https://texttospeech.googleapis.com/v1/text:synthesize?key=${GOOGLE_CLOUD_API_KEY}`,
            {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    input: { text: text },
                    voice: {
                        languageCode: 'ru-RU',
                        name: 'ru-RU-Wavenet-B', // Male voice, WaveNet quality (~$16 per 1M chars)
                        ssmlGender: 'MALE'
                    },
                    audioConfig: {
                        audioEncoding: 'MP3',
                        speakingRate: 0.85,
                        pitch: 0.0
                    }
                })
            }
        );

        if (!response.ok) {
            throw new Error(`Google Cloud TTS error: ${response.status} ${response.statusText}`);
        }

        const data = await response.json();
        console.log('✅ Audio received from Google Cloud TTS');

        // Decode base64 audio content
        const audioContent = data.audioContent;
        const audioBlob = new Blob(
            [Uint8Array.from(atob(audioContent), c => c.charCodeAt(0))],
            { type: 'audio/mp3' }
        );
        const audioUrl = URL.createObjectURL(audioBlob);

        // Create and play audio
        const audio = new Audio(audioUrl);

        // Wait for audio to be fully loaded
        await new Promise((resolve, reject) => {
            audio.oncanplaythrough = () => {
                console.log('✅ Google Cloud audio fully buffered');
                resolve();
            };

            audio.onerror = (e) => {
                console.error('❌ Google Cloud audio loading error:', e);
                reject(e);
            };

            audio.load();
        });

        // Add buffer delay
        await new Promise(resolve => setTimeout(resolve, 1000));

        // Play audio
        await audio.play();

        // Wait for playback to complete
        await new Promise((resolve) => {
            audio.onended = () => {
                console.log('✅ Google Cloud TTS playback completed');
                URL.revokeObjectURL(audioUrl);
                resolve();
            };
        });

    } catch (error) {
        console.error('❌ Google Cloud TTS error:', error);
        throw error; // Re-throw to trigger next fallback
    }
}

// Browser TTS fallback (Tier 3)
function playFallbackTTS(text) {
    return new Promise((resolve, reject) => {
        if (!speechSynthesis) {
            reject(new Error('Browser TTS not supported'));
            return;
        }

        // Get Russian voice (prefer GoogleTranslate for Chrome/Voice Out)
        const voices = speechSynthesis.getVoices();
        let russianVoice = voices.find(v => v.name.includes('GoogleTranslate') && v.lang.includes('ru')) ||
                           voices.find(v => v.name.includes('Dmitry') && v.lang.includes('ru')) ||
                           voices.find(v => v.name.includes('Google') && v.lang.includes('ru')) ||
                           voices.find(v => v.lang.includes('ru-RU') && v.name.toLowerCase().includes('male')) ||
                           voices.find(v => v.lang.includes('ru-RU')) ||
                           voices.find(v => v.lang.includes('ru'));

        console.log('🔊 Fallback voice:', russianVoice ? russianVoice.name : 'default');

        // Split text into chunks (browser limit ~200 chars)
        const maxChunkSize = 180;
        let chunks = [];

        if (text.length > maxChunkSize) {
            const sentences = text.split(/[.!?]\s+/);
            let currentChunk = '';

            sentences.forEach((sentence, idx) => {
                if (currentChunk.length + sentence.length < maxChunkSize) {
                    currentChunk += sentence + (idx < sentences.length - 1 ? '. ' : '');
                } else {
                    if (currentChunk) chunks.push(currentChunk.trim());
                    currentChunk = sentence + (idx < sentences.length - 1 ? '. ' : '');
                }
            });
            if (currentChunk) chunks.push(currentChunk.trim());
        } else {
            chunks = [text];
        }

        console.log('🔊 Split into', chunks.length, 'chunks for fallback TTS');

        let chunkIndex = 0;

        function speakNextChunk() {
            if (chunkIndex >= chunks.length) {
                console.log('✅ Fallback TTS completed all chunks');
                resolve();
                return;
            }

            const chunk = chunks[chunkIndex];
            chunkIndex++;

            const utterance = new SpeechSynthesisUtterance(chunk);
            utterance.lang = 'ru-RU';
            utterance.rate = 0.85;
            utterance.pitch = 1;
            utterance.volume = 1;

            if (russianVoice) {
                utterance.voice = russianVoice;
            }

            utterance.onend = () => {
                if (chunkIndex < chunks.length) {
                    setTimeout(speakNextChunk, 200);
                } else {
                    resolve();
                }
            };

            utterance.onerror = (event) => {
                console.error('❌ Fallback TTS chunk error:', event.error);
                reject(new Error('Fallback TTS error: ' + event.error));
            };

            speechSynthesis.speak(utterance);
        }

        // Start speaking first chunk
        speakNextChunk();
    });
}


