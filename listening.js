// Show listening questions
function showListeningQuestions() {
    console.log('=== showListeningQuestions called ===');
    const exercise = listeningExercises[currentListeningLevel][currentListeningIndex];
    const questionsContainer = document.getElementById('listening-questions');
    
    console.log('Exercise:', exercise.title);
    console.log('Questions count:', exercise.questions.length);
    
    document.getElementById('listening-instructions').style.display = 'none';
    questionsContainer.style.display = 'block';
    questionsContainer.innerHTML = '';
    
    exercise.questions.forEach((q, qIndex) => {
        const questionDiv = document.createElement('div');
        questionDiv.style.cssText = 'background: #f8f9ff; padding: 20px; border-radius: 10px; margin: 20px 0;';
        
        const questionText = document.createElement('div');
        questionText.style.cssText = 'font-size: 1.2em; font-weight: bold; margin-bottom: 15px; color: #333;';
        questionText.textContent = `${qIndex + 1}. ${q.question}`;
        questionDiv.appendChild(questionText);
        
        q.options.forEach((option, optIndex) => {
            const optionBtn = document.createElement('button');
            optionBtn.className = 'listening-option';
            optionBtn.style.cssText = 'width: 100%; padding: 12px 15px; margin: 6px 0; background: white; border: 2px solid #667eea; border-radius: 8px; font-size: 1em; cursor: pointer; text-align: left; transition: all 0.3s;';
            optionBtn.textContent = option;
            optionBtn.onclick = () => selectListeningAnswer(qIndex, optIndex, q.correct, optionBtn);
            optionBtn.onmouseover = () => optionBtn.style.background = '#f0f0f0';
            optionBtn.onmouseout = () => optionBtn.style.background = 'white';
            questionDiv.appendChild(optionBtn);
        });
        
        questionsContainer.appendChild(questionDiv);
    });
    
    console.log('Questions displayed');
}

// Select listening answer
function selectListeningAnswer(questionIndex, selectedOption, correctOption, button) {
    console.log('Answer selected:', questionIndex, 'selected:', selectedOption, 'correct:', correctOption);
    
    const questionDiv = button.parentElement;
    const allButtons = questionDiv.querySelectorAll('.listening-option');
    
    allButtons.forEach(btn => btn.disabled = true);
    
    totalListeningQuestions++;
    
    allButtons[correctOption].style.background = '#e8f5e9';
    allButtons[correctOption].style.borderColor = '#4caf50';
    allButtons[correctOption].style.fontWeight = 'bold';
    
    if (selectedOption !== correctOption) {
        allButtons[selectedOption].style.background = '#ffebee';
        allButtons[selectedOption].style.borderColor = '#f44336';
    } else {
        listeningCorrectAnswers++;
        console.log('Correct! Total correct:', listeningCorrectAnswers);
    }
    
    console.log('Total questions answered:', totalListeningQuestions);
    checkAllListeningAnswered();
}

// Check if all questions answered
function checkAllListeningAnswered() {
    console.log('=== Checking if all answered ===');
    
    const exercise = listeningExercises[currentListeningLevel][currentListeningIndex];
    const questionDivs = document.querySelectorAll('#listening-questions > div');
    
    let allAnswered = true;
    questionDivs.forEach((div, idx) => {
        const buttons = div.querySelectorAll('.listening-option');
        let answered = false;
        buttons.forEach(btn => {
            if (btn.disabled) answered = true;
        });
        if (!answered) allAnswered = false;
    });
    
    console.log('All answered:', allAnswered);
    
    if (allAnswered) {
        console.log('All questions answered! Showing complete button');
        
        // Show results immediately
        const percentage = Math.round((listeningCorrectAnswers / totalListeningQuestions) * 100);
        const resultDiv = document.createElement('div');
        resultDiv.style.cssText = 'background: linear-gradient(135deg, #667eea 0%, #764ba2 100%); color: white; padding: 20px; border-radius: 10px; margin: 30px 0; text-align: center;';
        resultDiv.innerHTML = `
            <div style="font-size: 1.5em; font-weight: bold; margin-bottom: 10px;">
                Your Score: ${listeningCorrectAnswers}/${totalListeningQuestions} (${percentage}%)
            </div>
            <div style="font-size: 1.1em; margin: 15px 0;">
                ${percentage >= 90 ? '🏆 Excellent!' : percentage >= 70 ? '⭐ Good job!' : '📚 Keep practicing!'}
            </div>
            <button onclick="completeExercise()" class="go-button" style="background: white; color: #667eea; font-size: 1.2em; padding: 15px 40px; margin-top: 15px;">
                ✅ Complete & Back to List
            </button>
        `;
        
        document.getElementById('listening-questions').appendChild(resultDiv);
    }
}

// Complete exercise and return to list
function completeExercise() {
    console.log('=== Completing exercise ===');
    
    const percentage = Math.round((listeningCorrectAnswers / totalListeningQuestions) * 100);
    
    // Save this specific exercise's score
    const progress = JSON.parse(localStorage.getItem('listeningProgress') || '{}');
    if (!progress[currentListeningLevel]) progress[currentListeningLevel] = {};
    
    progress[currentListeningLevel][currentListeningIndex] = {
        date: new Date().toLocaleDateString(),
        score: percentage,
        correct: listeningCorrectAnswers,
        total: totalListeningQuestions
    };
    
    localStorage.setItem('listeningProgress', JSON.stringify(progress));
    console.log('Saved progress:', progress);
    
    // Go back to exercise list
    showExerciseList(currentListeningLevel);
}

