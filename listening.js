// Load listening stats
function loadListeningStats() {
    const progress = JSON.parse(localStorage.getItem('listeningProgress') || '{}');
    console.log('Loading listening stats:', progress);

    let totalCompleted = 0;
    let totalScore = 0;

    // Count completed exercises across all levels
    for (let level in progress) {
        const levelData = progress[level];
        for (let exerciseIndex in levelData) {
            totalCompleted++;
            totalScore += levelData[exerciseIndex].score;
        }
    }

    console.log('Total completed:', totalCompleted, 'Total score:', totalScore);

    const attemptsEl = document.getElementById('listening-attempts');
    const avgScoreEl = document.getElementById('listening-avg-score');

    console.log('Elements found:', attemptsEl, avgScoreEl);

    if (totalCompleted > 0) {
        const avgScore = Math.round(totalScore / totalCompleted);
        console.log('Setting avgScore:', avgScore + '%', 'completed:', totalCompleted);

        if (avgScoreEl) avgScoreEl.textContent = avgScore + '%';
        if (attemptsEl) attemptsEl.textContent = totalCompleted;
    } else {
        console.log('No exercises completed yet');
        if (avgScoreEl) avgScoreEl.textContent = '0%';
        if (attemptsEl) attemptsEl.textContent = '0';
    }

}

