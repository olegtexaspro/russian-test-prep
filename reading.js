let currentReadingLevel = 'novice';
let currentPassageIndex = 0;
let readingStartTime = null;
let readingTimerInterval = null;
let readingCorrectAnswers = 0;
let totalReadingQuestions = 0;
let passageStartTime = 0;
let passageReadingTime = 0;

// Initialize reading practice
function initReading() {
    showReadingExerciseList('novice');
}

// Show exercise list
function showReadingExerciseList(level) {
    currentReadingLevel = level;
    
    // Hide content, show list
    document.getElementById('reading-content').style.display = 'none';
    document.getElementById('reading-exercise-list-container').style.display = 'block';
    
    // Load progress from localStorage
    const progress = JSON.parse(localStorage.getItem('readingProgress') || '{}');
    const levelProgress = progress[level] || {};
    
    // Build exercise list
    const passages = readingPassages[level];
    const listContainer = document.getElementById('reading-exercise-list');
    listContainer.innerHTML = '';
    
    passages.forEach((passage, index) => {
        const passageData = levelProgress[index];
        const completed = passageData ? true : false;
        const score = passageData ? passageData.score : 0;
        const time = passageData ? passageData.time : 0;
        
        const card = document.createElement('div');
        card.style.cssText = `
            background: ${completed ? 'linear-gradient(135deg, #e8f5e9 0%, #c8e6c9 100%)' : 'linear-gradient(135deg, #f5f5f5 0%, #e0e0e0 100%)'};
            padding: 20px;
            border-radius: 10px;
            margin: 15px 0;
            cursor: pointer;
            border: 2px solid ${completed ? '#4caf50' : '#bdbdbd'};
            transition: all 0.3s;
        `;
        
        card.onmouseover = () => card.style.transform = 'translateX(5px)';
        card.onmouseout = () => card.style.transform = 'translateX(0)';
        card.onclick = () => startReadingPassage(level, index);
        
        const minutes = Math.floor(time / 60);
        const seconds = time % 60;
        const timeStr = `${minutes}:${seconds.toString().padStart(2, '0')}`;
        
        card.innerHTML = `
            <div style="display: flex; align-items: center; justify-content: space-between;">
                <div style="flex: 1;">
                    <div style="font-size: 1.1em; font-weight: bold; color: #333; margin-bottom: 5px;">
                        ${completed ? '✅' : '⭕'} ${index + 1}. ${passage.title}
                    </div>
                    <div style="color: #666; font-size: 0.9em;">
                        ${passage.type} • ${passage.questions.length} questions
                    </div>
                </div>
                <div style="text-align: right;">
                    ${completed ? `
                        <div style="font-size: 1.3em; font-weight: bold; color: #4caf50;">${score}%</div>
                        <div style="color: #666; font-size: 0.9em; margin-top: 3px;">⏱️ ${timeStr}</div>
                    ` : ''}
                    <div style="color: #666; font-size: 0.85em; margin-top: 5px;">
                        ${completed ? 'Click to redo' : 'Click to start'}
                    </div>
                </div>
            </div>
        `;
        
        listContainer.appendChild(card);
    });
    
    // Update stats
    loadReadingStats();
}

// Change reading level
function changeReadingLevel(level) {
    showReadingExerciseList(level);
}

// Start specific passage
function startReadingPassage(level, index) {
    console.log('=== Starting passage', index + 1, '===');
    
    currentReadingLevel = level;
    currentPassageIndex = index;
    readingCorrectAnswers = 0;
    totalReadingQuestions = 0;
    
    // Hide list, show content
    document.getElementById('reading-exercise-list-container').style.display = 'none';
    document.getElementById('reading-content').style.display = 'block';
    
    loadCurrentPassage();
}

// Load current passage
function loadCurrentPassage() {
    const passage = readingPassages[currentReadingLevel][currentPassageIndex];
    
    console.log('Loading:', passage.title);
    
    // Display passage
    document.getElementById('passage-title').textContent = passage.title;
    document.getElementById('passage-type').textContent = `Type: ${passage.type}`;
    document.getElementById('passage-text').textContent = passage.text;
    
    // Start timer
    passageStartTime = Date.now();
    console.log('Timer started');
    
    // Clear and add questions
    const questionsContainer = document.getElementById('reading-questions');
    questionsContainer.innerHTML = '';
    
    passage.questions.forEach((q, qIndex) => {
        const questionDiv = document.createElement('div');
        questionDiv.style.cssText = 'background: #f8f9ff; padding: 20px; border-radius: 10px; margin: 20px 0;';
        
        const questionText = document.createElement('div');
        questionText.style.cssText = 'font-size: 1.2em; font-weight: bold; margin-bottom: 15px; color: #333;';
        questionText.textContent = `${qIndex + 1}. ${q.question}`;
        questionDiv.appendChild(questionText);
        
        q.options.forEach((option, optIndex) => {
            const optionBtn = document.createElement('button');
            optionBtn.className = 'reading-option';
            optionBtn.style.cssText = 'width: 100%; padding: 12px 15px; margin: 6px 0; background: white; border: 2px solid #667eea; border-radius: 8px; font-size: 1em; cursor: pointer; text-align: left; transition: all 0.3s;';
            optionBtn.textContent = option;
            optionBtn.onclick = () => selectReadingAnswer(qIndex, optIndex, q.correct, optionBtn);
            optionBtn.onmouseover = () => optionBtn.style.background = '#f0f0f0';
            optionBtn.onmouseout = () => optionBtn.style.background = 'white';
            questionDiv.appendChild(optionBtn);
        });
        
        questionsContainer.appendChild(questionDiv);
    });
}

// Select reading answer
function selectReadingAnswer(questionIndex, selectedOption, correctOption, button) {
    const questionDiv = button.parentElement;
    const allButtons = questionDiv.querySelectorAll('.reading-option');
    
    allButtons.forEach(btn => btn.disabled = true);
    
    totalReadingQuestions++;
    
    allButtons[correctOption].style.background = '#e8f5e9';
    allButtons[correctOption].style.borderColor = '#4caf50';
    allButtons[correctOption].style.fontWeight = 'bold';
    
    if (selectedOption !== correctOption) {
        allButtons[selectedOption].style.background = '#ffebee';
        allButtons[selectedOption].style.borderColor = '#f44336';
    } else {
        readingCorrectAnswers++;
    }
    
    checkAllQuestionsAnswered();
}

// Check if all questions answered
function checkAllQuestionsAnswered() {
    console.log('=== Checking if all answered ===');
    
    const passage = readingPassages[currentReadingLevel][currentPassageIndex];
    const questionDivs = document.querySelectorAll('#reading-questions > div');
    
    let allAnswered = true;
    questionDivs.forEach(div => {
        const buttons = div.querySelectorAll('.reading-option');
        let answered = false;
        buttons.forEach(btn => {
            if (btn.disabled) answered = true;
        });
        if (!answered) allAnswered = false;
    });
    
    console.log('All answered:', allAnswered);
    
    if (allAnswered) {
        console.log('All questions answered!');
        
        // Stop timer
        const endTime = Date.now();
        passageReadingTime = Math.round((endTime - passageStartTime) / 1000);
        console.log('Reading time:', passageReadingTime, 'seconds');
        
        // Calculate score
        const percentage = Math.round((readingCorrectAnswers / totalReadingQuestions) * 100);
        
        const minutes = Math.floor(passageReadingTime / 60);
        const seconds = passageReadingTime % 60;
        const timeStr = `${minutes}:${seconds.toString().padStart(2, '0')}`;
        
        // Show results immediately
        const resultDiv = document.createElement('div');
        resultDiv.style.cssText = 'background: linear-gradient(135deg, #667eea 0%, #764ba2 100%); color: white; padding: 20px; border-radius: 10px; margin: 30px 0; text-align: center;';
        resultDiv.innerHTML = `
            <div style="font-size: 1.5em; font-weight: bold; margin-bottom: 10px;">
                Your Score: ${readingCorrectAnswers}/${totalReadingQuestions} (${percentage}%)
            </div>
            <div style="font-size: 1.2em; margin-bottom: 10px;">
                ⏱️ Reading Time: ${timeStr}
            </div>
            <div style="font-size: 1.1em; margin: 15px 0;">
                ${percentage >= 90 ? '🏆 Excellent!' : percentage >= 70 ? '⭐ Good job!' : '📚 Keep practicing!'}
            </div>
            <button onclick="completeReadingPassage()" class="go-button" style="background: white; color: #667eea; font-size: 1.2em; padding: 15px 40px; margin-top: 15px;">
                ✅ Complete & Back to List
            </button>
        `;
        
        document.getElementById('reading-questions').appendChild(resultDiv);
    }
}

// Complete passage and return to list
function completeReadingPassage() {
    console.log('=== Completing passage ===');
    
    const percentage = Math.round((readingCorrectAnswers / totalReadingQuestions) * 100);
    
    // Save this specific passage's score and time
    const progress = JSON.parse(localStorage.getItem('readingProgress') || '{}');
    if (!progress[currentReadingLevel]) progress[currentReadingLevel] = {};
    
    progress[currentReadingLevel][currentPassageIndex] = {
        date: new Date().toLocaleDateString(),
        score: percentage,
        correct: readingCorrectAnswers,
        total: totalReadingQuestions,
        time: passageReadingTime
    };
    
    localStorage.setItem('readingProgress', JSON.stringify(progress));
    console.log('Saved progress:', progress);
    
    // Go back to exercise list
    showReadingExerciseList(currentReadingLevel);
}

// Back to exercise list
function backToReadingList() {
    showReadingExerciseList(currentReadingLevel);
}

// Load reading stats
function loadReadingStats() {
    const progress = JSON.parse(localStorage.getItem('readingProgress') || '{}');
    console.log('Loading reading stats:', progress);
    
    let totalCompleted = 0;
    let totalScore = 0;
    let totalTime = 0;
    
    // Count completed passages across all levels
    for (let level in progress) {
        const levelData = progress[level];
        for (let passageIndex in levelData) {
            totalCompleted++;
            totalScore += levelData[passageIndex].score;
            totalTime += levelData[passageIndex].time || 0;
        }
    }
    
    console.log('Total completed:', totalCompleted, 'Total score:', totalScore, 'Total time:', totalTime);
    
    const attemptsEl = document.getElementById('reading-attempts');
    const avgScoreEl = document.getElementById('reading-avg-score');
    const avgTimeEl = document.getElementById('reading-avg-time');
    
    if (totalCompleted > 0) {
        const avgScore = Math.round(totalScore / totalCompleted);
        const avgTime = Math.round(totalTime / totalCompleted);
        const minutes = Math.floor(avgTime / 60);
        const seconds = avgTime % 60;
        const timeStr = `${minutes}:${seconds.toString().padStart(2, '0')}`;
        
        console.log('Setting avgScore:', avgScore + '%', 'completed:', totalCompleted, 'avgTime:', timeStr);
        
        if (avgScoreEl) avgScoreEl.textContent = avgScore + '%';
        if (attemptsEl) attemptsEl.textContent = totalCompleted;
        if (avgTimeEl) avgTimeEl.textContent = timeStr;
    } else {
        console.log('No passages completed yet');
        if (avgScoreEl) avgScoreEl.textContent = '0%';
        if (attemptsEl) attemptsEl.textContent = '0';
        if (avgTimeEl) avgTimeEl.textContent = '0:00';
    }
}
